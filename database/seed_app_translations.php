$inserted = 0;
$skipped = 0;

foreach ($translations as $name => $value) {
    $exists = $ToryHub->get_row_safe(
        "SELECT `id` FROM `translate` WHERE `lang_id` = ? AND `name` = ?",
        [$zhId, $name]
    );
    if ($exists) {
        $skipped++;
        continue;
    }
    $ToryHub->insert_safe('translate', [
        'lang_id' => $zhId,
        'name' => $name,
        'value' => $value,
    ]);
    $inserted++;
}

