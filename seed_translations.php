$inserted = 0;
$updated = 0;
$skipped = 0;

foreach ($translations as $key => $langs) {
    foreach (['zh' => $zhId, 'en' => $enId] as $langCode => $langId) {
        $value = $langs[$langCode] ?? '';
        if ($value === '') continue;

        $existing = $ToryHub->get_row_safe(
            "SELECT * FROM `translate` WHERE `lang_id` = ? AND `name` = ?",
            [$langId, $key]
        );

        if ($existing) {
            if ($existing['value'] !== $value) {
                $ToryHub->update_safe('translate', ['value' => $value], 'id = ?', [$existing['id']]);
                $updated++;
            } else {
                $skipped++;
            }
        } else {
            $ToryHub->insert_safe('translate', [
                'lang_id' => $langId,
                'name' => $key,
                'value' => $value
            ]);
            $inserted++;
        }
    }
}

